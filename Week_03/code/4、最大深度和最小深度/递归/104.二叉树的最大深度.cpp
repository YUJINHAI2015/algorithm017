/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    int maxDepth(TreeNode* root) {
        // 最大深度
        // 递归算法 
        // 1、确定参数和返回值
        // 2、确定终止条件
        // 3、确定单层逻辑

        if (!root) return 0;
        int left = maxDepth(root->left);
        int right = maxDepth(root->right);        

        return max(left,right) + 1;

    }



};
